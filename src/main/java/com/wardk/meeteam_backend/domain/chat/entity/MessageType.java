package com.wardk.meeteam_backend.domain.chat.entity;

/**
 * 채팅 메시지의 타입을 정의하는 열거형입니다.
 *
 * <p>다양한 형태의 메시지를 지원하며, 향후 확장 가능하도록 설계되었습니다:</p>
 *
 * @author MeeTeam Backend Team
 * @version 1.0
 * @since 1.0
 */
public enum MessageType {

    /**
     * 일반 텍스트 메시지입니다.
     *
     * <p>가장 기본적인 메시지 타입으로, 사용자가 입력한 텍스트 내용을 전달합니다.</p>
     */
    TEXT,

    /**
     * 이미지 메시지입니다.
     *
     * <p>이미지 파일을 첨부한 메시지입니다.
     * 향후 구현 예정인 기능으로, 이미지 URL이나 파일 정보가 포함됩니다.</p>
     */
    IMAGE,

    /**
     * 파일 메시지입니다.
     *
     * <p>문서, 압축 파일 등 다양한 형태의 파일을 첨부한 메시지입니다.
     * 향후 구현 예정인 기능으로, 파일명, 크기, 다운로드 URL 등의 정보가 포함됩니다.</p>
     */
    FILE,

    /**
     * 시스템 메시지입니다.
     *
     * <p>사용자가 직접 작성한 메시지가 아닌, 시스템에서 자동으로 생성하는 메시지입니다.
     * 예: "사용자가 입장했습니다", "사용자가 퇴장했습니다" 등</p>
     */
    SYSTEM,

    /**
     * 멘션이 포함된 메시지입니다.
     *
     * <p>@username 형태로 특정 사용자를 언급한 메시지입니다.
     * 멘션된 사용자에게는 별도의 알림이 발송됩니다.</p>
     */
    MENTION
}
